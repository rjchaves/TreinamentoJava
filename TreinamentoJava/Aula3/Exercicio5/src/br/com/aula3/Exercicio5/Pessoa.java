package br.com.aula3.Exercicio5;

public class Pessoa {
	private char sexo;
	private char resposta;
	
	
	
	public Pessoa(char sexo, char resposta) {
		super();
		this.sexo = sexo;
		this.resposta = resposta;
	}
	
	public char getSexo() {
		return sexo;
	}
	public char getResposta() {
		return resposta;
	}
}
